package com.sl.railbuddy;

public class CURAVL
{
    String onea, twoa, threea, sl, cc;
    public CURAVL()
    {
    }
    public CURAVL(String onea, String twoa, String threea, String sl, String cc)
    {
        this.onea = onea;
        this.twoa = twoa;
        this.threea = threea;
        this.sl = sl;
        this.cc = cc;
    }

    public String getOnea()
    {
        return onea;
    }

    public void setOnea(String onea)
    {
        this.onea = onea;
    }

    public String getTwoa()
    {
        return twoa;
    }

    public void setTwoa(String twoa)
    {
        this.twoa = twoa;
    }

    public String getThreea()
    {
        return threea;
    }

    public void setThreea(String threea)
    {
        this.threea = threea;
    }

    public String getSl()
    {
        return sl;
    }

    public void setSl(String sl)
    {
        this.sl = sl;
    }

    public String getCc()
    {
        return cc;
    }

    public void setCc(String cc)
    {
        this.cc = cc;
    }
}
